## lambda function ##

sum=lambda x,y:x+y
print(sum(1,5))

sub=lambda x,y: x-y
print(sub(1,4))


pow=lambda x,y: x**y
print(pow(2,4))


div=lambda x,y: x/y
print(div(54,9))


a=int(input('Enter first number- '))
